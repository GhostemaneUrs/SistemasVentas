    void listarVendedor() {
        modelo = (DefaultTableModel) tablaVendedor.getModel();
        List<vendedor> lista = datos.listarVendedor();
        Object[] object = new Object[6];
        for (int i = 0; i < lista.size(); i++) {
            object[0] = lista.get(i).getIdVendedor();
            object[1] = lista.get(i).getDni();
            object[2] = lista.get(i).getNombre();
            object[3] = lista.get(i).getTelefono();
            object[4] = lista.get(i).getEstado();
            object[5] = lista.get(i).getUsuario();
            modelo.addRow(object);
        }
        tablaVendedor.setModel(modelo);
    }
