    void agregarVendedor() {
        String nom = txtNombreVendedor.getText();
        String dni = txtDNIVendedor.getText();
        String tel = txtTelefonoVendedor.getText();
        String estado = jbcEstadoVendedor.getSelectedItem().toString();
        String user = nom.substring(0, 2).concat(dni.substring(0, 2));
        if (txtNombreVendedor.getText().equals("") || txtDNIVendedor.getText().equals("") || txtTelefonoVendedor.getText().equals("")) {
            JOptionPane.showMessageDialog(this, "Ingrese datos, por favor");
            txtNombreVendedor.requestFocus();
            txtDNIVendedor.requestFocus();
            txtTelefonoVendedor.requestFocus();
        } else {
        vd.setDni(dni);
        vd.setNombre(nom);
        vd.setTelefono(tel);
        vd.setEstado(estado);
        vd.setUsuario(user);
        datos.agregarVendedor(vd);
        r=datos.actualizarVendedor(vd);
    }if(r==0){
        JOptionPane.showMessageDialog(this, "Vendedor agregado con exito..!!");
    }else{
        JOptionPane.showMessageDialog(this, "Error");
    }
  }
    
    void eliminarVendedor(){
        int fila = tablaVendedor.getSelectedRow();
        if (fila == -1) {
            JOptionPane.showMessageDialog(this, "Debe Seleccionar una Fila");
        } else {
            int id = Integer.parseInt(tablaVendedor.getValueAt(fila, 0).toString());
            datos.eliminarVendedor(id);
            JOptionPane.showMessageDialog(this, "Vendedor Eliminado");
        } 
    }
    
    void modificarVendedor() {
        int fila = tablaVendedor.getSelectedRow();
        if (fila == -1) {
            JOptionPane.showMessageDialog(this, "Debe seleccionar una fila");
        } else {
            int id = Integer.parseInt(modelo.getValueAt(fila, 0).toString());
            String dni = txtDNIVendedor.getText();
            String nom = txtNombreVendedor.getText();            
            String tel=txtTelefonoVendedor.getText();
            String estado = jbcEstadoVendedor.getSelectedItem().toString();            
            vd.setDni(dni);
            vd.setNombre(nom);
            vd.setTelefono(tel);
            vd.setEstado(estado);
            vd.setIdVendedor(id);
            r=datos.actualizarVendedor(vd);
            if(r==1){
                JOptionPane.showMessageDialog(this, "Vendedor actualizado con exito..!!");
            }
            else{
                JOptionPane.showMessageDialog(this, "Error");
            } 
        }
    }

    void nuevo() {
        txtNombreVendedor.setText("");
        txtDNIVendedor.setText("");
        txtTelefonoVendedor.setText("");
        jbcEstadoVendedor.setSelectedIndex(0); 
        txtDNIVendedor.requestFocus();
    }

    void LimpiarTabla() {
        for (int i = 0; i < modelo.getRowCount(); i++) {
            modelo.removeRow(i);
            i = i - 1;
        }
    }
   
