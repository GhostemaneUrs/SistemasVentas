    void listar() {
        List<cliente> listar = datos.listar();
        modelo = (DefaultTableModel) tblCliente.getModel();
        Object[] ob = new Object[5];
        for (int i = 0; i < listar.size(); i++) {
            ob[0] = listar.get(i).getId();
            ob[1] = listar.get(i).getDni();
            ob[2] = listar.get(i).getNombre();
            ob[3] = listar.get(i).getDireccion();
            ob[4] = listar.get(i).getEstado();
            modelo.addRow(ob);
        }
        tblCliente.setModel(modelo);
    }

