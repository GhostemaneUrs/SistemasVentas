    private void tblClienteMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_tblClienteMouseClicked
        int fila = tblCliente.getSelectedRow();
        if (fila == -1) {
            JOptionPane.showMessageDialog(this, "Debe Seleccionar una Fila");
        } else {
            id = Integer.parseInt(tblCliente.getValueAt(fila, 0).toString());
            String dni = tblCliente.getValueAt(fila, 1).toString();
            String nom = tblCliente.getValueAt(fila, 2).toString();
            String dir = tblCliente.getValueAt(fila, 3).toString();
            String es = tblCliente.getValueAt(fila, 4).toString();
            txtDNI.setText(dni);
            txtNombreCompleto.setText(nom);
            txtDireccion.setText(dir);
            jcbEstadoCLiente.setSelectedItem(es);
    }//GEN-LAST:event_tblClienteMouseClicked
    }

    public void agregarCliente() {
        String dni = txtDNI.getText();
        String nom = txtNombreCompleto.getText();
        String dir = txtDireccion.getText();
        String es = jcbEstadoCLiente.getSelectedItem().toString();
        if (txtDNI.getText().equals("") || txtNombreCompleto.getText().equals("") || txtDireccion.getText().equals("")) {
            JOptionPane.showMessageDialog(this, "Ingrese datos, por favor");
            txtDNI.requestFocus();
            txtNombreCompleto.requestFocus();
            txtDireccion.requestFocus();
        } else {
            Object[] ob = new Object[4];
            ob[0] = dni;
            ob[1] = nom;
            ob[2] = dir;
            ob[3] = es;
            datos.agregar(ob);
        }
    }
 
    void actualizarCliente() {
        int fila = tblCliente.getSelectedRow();
        if (fila == -1) {
            JOptionPane.showMessageDialog(this, "Debe selecionar una Fila");
        } else {
            String dni = txtDNI.getText();
            String nom = txtNombreCompleto.getText();
            String dir = txtDireccion.getText();
            String es = jcbEstadoCLiente.getSelectedItem().toString();
            Object[] obj = new Object[5];
            obj[0] = dni;
            obj[1] = nom;
            obj[2] = dir;
            obj[3] = es;
            obj[4] = id;
            datos.actualizar(obj);
        }

    }

    void eliminarCliente() {
        int fila = tblCliente.getSelectedRow();
        if (fila == -1) {
            JOptionPane.showMessageDialog(this, "Debe seleccionar una Fila");
        } else {
            datos.eliminar(id);
        }
    }

    void nuevoCliente() {
        txtNombreCompleto.setText("");
        txtDireccion.setText("");
        txtDNI.setText("");
        jcbEstadoCLiente.setSelectedItem("Seleccionar");
        txtDNI.requestFocus();
    }

    void limpiarTabla() {
        for (int i = 0; i < modelo.getRowCount(); i++) {
            modelo.removeRow(i);
            i = i - 1;
        }
    }

